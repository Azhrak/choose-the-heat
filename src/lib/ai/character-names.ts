/**
 * Select a random character name based on gender preference
 */
export function selectCharacterName(gender: string | undefined): string | null {
	if (!gender) return null;

	let nameList: string[];

	switch (gender.toLowerCase()) {
		case "female":
		case "trans-woman":
			nameList = FEMALE_NAMES;
			break;
		case "male":
		case "trans-man":
			nameList = MALE_NAMES;
			break;
		case "non-binary":
		case "genderqueer":
		case "agender":
		case "genderfluid":
			nameList = NON_BINARY_NAMES;
			break;
		default:
			return null;
	}

	// Select random name from list
	const randomIndex = Math.floor(Math.random() * nameList.length);
	return nameList[randomIndex];
}

